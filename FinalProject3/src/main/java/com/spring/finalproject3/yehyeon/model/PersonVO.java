package com.spring.finalproject3.yehyeon.model;

public class PersonVO {
	
	private int perno;
	private String name;
	private int rcheck;
	
	
	public int getRcheck() {
		return rcheck;
	}
	public void setRcheck(int rcheck) {
		this.rcheck = rcheck;
	}
	public int getPerno() {
		return perno;
	}
	public void setPerno(int perno) {
		this.perno = perno;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}

}
